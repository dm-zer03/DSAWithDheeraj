
public class Conditionals {

    @SuppressWarnings("unused")
    public static void main(String[] args) {

        // Simple if 
        if (true) {
            System.out.println("Simple If Executed");
        }

        //CodeStyleSheetsOfCompanies: Set Of Rule to write a code for specific CodingLangauage.
        // if-else 
        if (false) {
            System.out.println("If Block Executed ...");
        } else {
            System.out.println("Else Block executed ....");
        }

        // else-if : Saves the Computational time of complier 
        if (false) {
            System.out.println("from else if : If Block Executed ...");
        } else if (false) {
            System.out.println("Else if  Block executed ....");
        } else {
            System.out.println("Else from else if  Block executed ....");
        }

    }
}
