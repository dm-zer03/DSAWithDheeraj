
public class LogicalOperators {

    public static void main(String[] args) {

        // Logical AND (&&)
        System.out.println(((3 > 2) && (5 > 0)));

        // Logical OR (||)
        System.out.println(((3 < 2) || (5 > 0)));

        //Logical Not (!)
        System.out.println(!((3 > 2) && (5 > 0)));
    }
}
